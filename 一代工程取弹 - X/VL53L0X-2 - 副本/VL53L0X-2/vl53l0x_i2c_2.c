//VL53L0X I2C初始化
void VL53L0X_i2c_init_2(void)
{
	GPIO_InitTypeDef 		GPIO_InitStructure;
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOD, ENABLE);//使能GPIOB时钟
	 
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8|GPIO_Pin_9;//端口配置
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;//普通输出模式
    GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;//推挽输出
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;//100MHz
    GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;//上拉
    GPIO_Init(GPIOD, &GPIO_InitStructure);//初始化

	GPIO_SetBits(GPIOD,GPIO_Pin_8|GPIO_Pin_9);//PB10,PB11 输出高
}

//产生IIC起始信号
void VL_IIC_Start_2(void)
{
	VL_SDA_OUT_2();//sda线输出
	VL_IIC_SDA_2=1;	  	  
	VL_IIC_SCL_2=1;
	delay_us(4);
 	VL_IIC_SDA_2=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	VL_IIC_SCL_2=0;//钳住I2C总线，准备发送或接收数据 
	
}

//产生IIC停止信号
void VL_IIC_Stop_2(void)
{
	VL_SDA_OUT_2();//sda线输出
	VL_IIC_SCL_2=0;
	VL_IIC_SDA_2=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	VL_IIC_SCL_2=1; 
	VL_IIC_SDA_2=1;//发送I2C总线结束信号
	delay_us(4);			
	
	
}

//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 VL_IIC_Wait_Ack_2(void)
{
	u8 ucErrTime=0;
	VL_SDA_IN_2();  //SDA设置为输入  
	VL_IIC_SDA_2=1;delay_us(1);	   
	VL_IIC_SCL_2=1;delay_us(1);	 
	while(VL_READ_SDA_2)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			VL_IIC_Stop_2();
			return 1;
		}
	}
	VL_IIC_SCL_2=0;//时钟输出0 	 
	
	return 0;  
}

//产生ACK应答
void VL_IIC_Ack_2(void)
{
	VL_IIC_SCL_2=0;
	VL_SDA_OUT_2();
	VL_IIC_SDA_2=0;
	delay_us(2);
	VL_IIC_SCL_2=1;
	delay_us(2);
	VL_IIC_SCL_2=0;
		

}

//不产生ACK应答		    
void VL_IIC_NAck_2(void)
{
	VL_IIC_SCL_2=0;
	VL_SDA_OUT_2();
	VL_IIC_SDA_2=1;
	delay_us(2);
	VL_IIC_SCL_2=1;
	delay_us(2);
	VL_IIC_SCL_2=0;
		

}

//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void VL_IIC_Send_Byte_2(u8 txd)
{           
	
	
    u8 t;   
	VL_SDA_OUT_2(); 	    
    VL_IIC_SCL_2=0;//拉低时钟开始数据传输
    for(t=0;t<8;t++)
    {              
		if((txd&0x80)>>7)
			VL_IIC_SDA_2=1;
		else
			VL_IIC_SDA_2=0;
		txd<<=1; 	  
		delay_us(2);  
		VL_IIC_SCL_2=1;
		delay_us(2); 
		VL_IIC_SCL_2=0;	
		delay_us(2);
    }
}



//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u8 VL_IIC_Read_Byte_2(unsigned char ack)
{
	
	unsigned char i,receive=0;
	

		VL_SDA_IN_2();//SDA设置为输入
		for(i=0;i<8;i++ )
		{
			VL_IIC_SCL_2=0; 
			delay_us(4);
		  VL_IIC_SCL_2=1;
			receive<<=1;
			if(VL_READ_SDA_2)receive++;   
		  delay_us(4); //1
		}					 
		if (!ack)
			VL_IIC_NAck_2();//发送nACK
		else


		VL_IIC_Ack_2(); //发送ACK   
		return receive;

}

//IIC写一个字节数据
u8 VL_IIC_Write_1Byte_2(u8 SlaveAddress, u8 REG_Address,u8 REG_data)
{
		

	VL_IIC_Start_2();
	VL_IIC_Send_Byte_2(SlaveAddress);
	if(VL_IIC_Wait_Ack_2())
	{
		VL_IIC_Stop_2();//释放总线
		return 1;//没应答则退出

	}
	VL_IIC_Send_Byte_2(REG_Address);
	VL_IIC_Wait_Ack_2();	
	VL_IIC_Send_Byte_2(REG_data);
	VL_IIC_Wait_Ack_2();	
	VL_IIC_Stop_2();

	return 0;
}

//IIC读一个字节数据
u8 VL_IIC_Read_1Byte_2(u8 SlaveAddress, u8 REG_Address,u8 *REG_data)
{
		

	VL_IIC_Start_2();
	VL_IIC_Send_Byte_2(SlaveAddress);//发写命令
	if(VL_IIC_Wait_Ack_2())
	{
		 VL_IIC_Stop_2();//释放总线
		 return 1;//没应答则退出
	}		
	VL_IIC_Send_Byte_2(REG_Address);
	VL_IIC_Wait_Ack_2();
	VL_IIC_Start_2(); 
	VL_IIC_Send_Byte_2(SlaveAddress|0x01);//发读命令
	VL_IIC_Wait_Ack_2();
	*REG_data = VL_IIC_Read_Byte_2(0);
	VL_IIC_Stop_2();

	return 0;
}

//IIC写n字节数据
u8 VL_IIC_Write_nByte_2(u8 SlaveAddress, u8 REG_Address,u16 len, u8 *buf)
{
		

	VL_IIC_Start_2();
	VL_IIC_Send_Byte_2(SlaveAddress);//发写命令
	if(VL_IIC_Wait_Ack_2()) 
	{
		VL_IIC_Stop_2();//释放总线
		return 1;//没应答则退出
	}
	VL_IIC_Send_Byte_2(REG_Address);
	VL_IIC_Wait_Ack_2();
	while(len--)
	{
		VL_IIC_Send_Byte_2(*buf++);//发送buff的数据
		VL_IIC_Wait_Ack_2();	
	}
	VL_IIC_Stop_2();//释放总线

	return 0;
	
}

//IIC读n字节数据
u8 VL_IIC_Read_nByte_2(u8 SlaveAddress, u8 REG_Address,u16 len,u8 *buf)
{
	

	VL_IIC_Start_2();
	VL_IIC_Send_Byte_2(SlaveAddress);//发写命令
	if(VL_IIC_Wait_Ack_2()) 
	{
		VL_IIC_Stop_2();//释放总线
		return 1;//没应答则退出
	}
	VL_IIC_Send_Byte_2(REG_Address);
	VL_IIC_Wait_Ack_2();

	VL_IIC_Start_2();
	VL_IIC_Send_Byte_2(SlaveAddress|0x01);//发读命令
	VL_IIC_Wait_Ack_2();
	while(len)
	{
		if(len==1)
		{
			*buf = VL_IIC_Read_Byte_2(0);
		}
		else
		{
			*buf = VL_IIC_Read_Byte_2(1);
		}
		buf++;
		len--;
	}
	VL_IIC_Stop_2();//释放总线

	return 0;
	
}


